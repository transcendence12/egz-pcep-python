# Which of the following statements is false?

# The right argument of the % operator can not be zero.
# The ** operator has right-to-left associativity.
# Multiplication precedes addition.
# The result of the / operator is always an integer value. - PRAWIDŁOWA ODPOWIEDŹ





# The right argument of the % operator can not be zero.

# To jest prawda, bo modulo dzielenie przez zero daje ZeroDivisionError.

# The ** operator has right-to-left associativity.

# To też jest prawda. Operator ** jest prawo-łączny.

# Multiplication precedes addition.

# Tak, w Pythonie mnożenie ma wyższy priorytet niż dodawanie, więc najpierw jest mnożenie, potem dodawanie.

# ✅ Prawda.

# The result of the / operator is always an integer value.

# Fałsz. W Pythonie a / b zawsze daje float, nawet jeśli dzielisz liczby całkowite.

# Przykład:

# 4 / 2  # wynik: 2.0 (float)
# 5 / 2  # wynik: 2.5


# Odpowiedź: fałszywe zdanie to ostatnie:

# The result of the / operator is always an integer value. ✅

