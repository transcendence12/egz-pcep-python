# An operator able to check whether two values are not equal is coded as:



# Opcje:
# <>
# != - PRAWIDŁOWA ODPOWIEDŹ
# =/=
# not ==

# Pythonie operator "różne od" (not equal) zapisujemy jako:

# !=

# 🔍 Omówmy opcje:

# <> → ❌ stare czasy (w Pythonie 2 działało, w Pythonie 3 już nie istnieje).

# != → ✅ poprawny zapis w Pythonie 3.

# =/= → ❌ coś takiego nie istnieje w Pythonie.

# not == → ❌ nie jest operatorem, choć składniowo not (a == b) by zadziałało, ale to nie jest standardowy zapis.

# ✅ Prawidłowa odpowiedź:
# !=
