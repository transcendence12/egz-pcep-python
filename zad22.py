# A data structure described as LIFO is actually a:

# stack
# tree
# list
# heap


# 1️⃣ Co to znaczy LIFO?

# LIFO = Last In, First Out

# Po polsku: ostatni wszedł, pierwszy wyszedł.

# To sposób działania struktury danych, w której ostatni dodany element jest pierwszym, który zostanie usunięty.

# 2️⃣ Sprawdźmy opcje:

# stack (stos) ✅

# To dokładnie LIFO.

# Wyobraź sobie talerz: kładziesz talerze jeden na drugim, ostatni talerz, który położyłeś, jest pierwszym, który zabierzesz.

# Pasuje idealnie do definicji LIFO.

# tree (drzewo) ❌

# Drzewo to struktura hierarchiczna z węzłami i dziećmi.

# Nie działa według LIFO ani FIFO.

# list (lista) ❌

# Lista to ogólna kolekcja elementów.

# Możesz ją używać jak LIFO lub FIFO, ale lista sama w sobie nie jest LIFO.

# heap (kopiec) ❌

# Heap to struktura, która utrzymuje porządek wg wartości (największy lub najmniejszy element).

# To nie LIFO, tylko priorytetowa kolejka.