# An alternative name for a data structure called a stack is:

# LIFO - to prawidlowa odp
# FIFO
# FOLO


# 1️⃣ Co to jest stack (stos)?

# Stack to struktura danych, która działa LIFO – Last In, First Out (ostatni wszedł, pierwszy wyszedł).

# Wyobraź sobie stos talerzy: kładziesz jeden na drugi, ostatni talerz położony na górze będzie pierwszym, który zabierzesz.

# 2️⃣ Wyjaśnienie opcji:

# LIFO ✅

# Dokładnie to samo, co stack.

# “Ostatni wszedł, pierwszy wyszedł”

# FIFO ❌

# First In, First Out – działa jak kolejka, a nie stos.

# Pierwszy element dodany jest też pierwszy usunięty.

# FOLO ❌

# Nie istnieje w terminologii informatycznej.